    private void btnEditAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditAddItemActionPerformed
      try {
            if( !txtMerkMobilAddItem.getText().isEmpty() && !txtTypeMobilAddItem.getText().isEmpty() && !txtNopolAddItem.getText().isEmpty() && !txtHargaAddItem.getText().isEmpty()){
                String kolom[] = {"merek","tipe","tahun","nopol","harga","status"};
                String isi[] = { txtMerkMobilAddItem.getText(),txtTypeMobilAddItem.getText(),comboTahun.getSelectedItem().toString(),txtNopolAddItem.getText(),txtHargaAddItem.getText(),comboStatus.getSelectedItem().toString()};
                con.queryUpdate("tb_mobil", kolom, isi,"id_mobil='"+String.valueOf(tablemobil.getValueAt(tablemobil.getSelectedRow(),0))+"'");
                JOptionPane.showMessageDialog(this, "Data Berhasil Diedit");
            }else{
                JOptionPane.showMessageDialog(this, "Data isian ada yang kosong");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error edit data");
        }
        loadTabel();
        clear();
    }//GEN-LAST:event_btnEditAddItemActionPerformed

