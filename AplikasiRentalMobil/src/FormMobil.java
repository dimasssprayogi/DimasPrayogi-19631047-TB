    private void btnSearchAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchAddItemActionPerformed
      if(txtSearchAddItem.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Isikan data pencarian");
        }else{
            try {
                rs=con.querySelectAll("tb_mobil", "id_mobil LIKE '%"+txtSearchAddItem.getText()+"%' OR merek LIKE '%"+txtSearchAddItem.getText()+"%' OR tipe LIKE '%"+txtSearchAddItem.getText()+"%' OR nopol LIKE '%"+txtSearchAddItem.getText()+"%' OR harga LIKE '%"+txtSearchAddItem.getText()+"%' OR tahun LIKE '%"+txtSearchAddItem.getText()+"%'" );
                tablemobil.setModel(new Database.ResultSetTable(rs));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Pencarian Error");
            }
        }
        clear();
    }//GEN-LAST:event_btnSearchAddItemActionPerformed

    private void btnDeleteAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteAddItemActionPerformed
      try {
            String id=String.valueOf(tablemobil.getValueAt(tablemobil.getSelectedRow(),0));
                if (JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data!!", "Warning", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
            con.queryDelete("tb_mobil","id_mobil="+id);

            }else{
                return;
            }
        } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Pilih id terlebih dahulu");
        }
        loadTabel();
        clear();
    }//GEN-LAST:event_btnDeleteAddItemActionPerformed

